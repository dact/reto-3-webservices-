/*
 * Metodos para realizar operaciones de resta y division.
 */

package logica;

/**
 *
 * @author Giovanny Escobar Uribe
 *         Andres Aguirre
 *         Duban Cano
 */
public class Calculadora2 {
    
    public Calculadora2(){
    }
    
    /**
     * Resta dos numeros.
     * 
     * @param a
     * @param b
     * @return 
     */
    public int restar(int a, int b) {
        return a - b;
    }
    
    /**
     * Divide dos numeros.
     * 
     * @param a
     * @param b
     * @return 
     */
    public int dividir(int a, int b) {
        return a / b;
    }
}
